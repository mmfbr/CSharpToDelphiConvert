						else if (accessorDeclarationSyntax.ExpressionBody != null)
						{
							output.Indent++;
                            output.Write("Result := ");
                            GenerateExpressionSyntax(accessorDeclarationSyntax.ExpressionBody.Expression);
                            output.WriteLine(";");
                            output.Indent--;
                        }

                        output.WriteLine("end;");
					}

					output.WriteLine(") then");
					output.Indent++;
					output.Write("FreeAndNil(");
					output.Write(CodeGenUtils.CreateEscapedIdentifier(e.Identifier.ValueText));
					output.WriteLine(");");
					output.Indent--;
				}



			}

		}


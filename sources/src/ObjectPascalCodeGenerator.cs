			{
				String[] p1 = pTypeName.Split('[');
				return $"TArray<{CSharpToObjectPascalType(p1[0])}>";
			}
			//if (pTypeName.Contains("<"))
			//{
			//	String[] p1 = pTypeName.Split('<');
			//	String[] p2 = p1[1].Split(new[] { ">" }, StringSplitOptions.RemoveEmptyEntries);
			//	p2 = p2[0].Split(new[] { "," }, StringSplitOptions.RemoveEmptyEntries);

			//	return $"{CSharpToObjectPascalType(p1[0])}<{String.Join(", ", p2.Select(v => CSharpToObjectPascalType(v)).ToArray())}>";
			//}
			switch (pTypeName)
			{
				case "int":
				case "Int":
					return "Integer";

				case "uint":
				case "UInt":
					return "UInt32";

				case "nint":
				case "NInt":
					return "NativeInt";

				case "nuint":
				case "NUInt":
					return "NativeUInt";

				case "Boolean":
				case "bool":
					return "Boolean";

				case "Array":
					return "TArray";

				case "StringBuilder":
					return "TStringBuilder";

				case "DateTime":
					return "TDatetime";

				case "long":
				case "Long":
					return "Int64";

				case "ulong":
				case "ULong":
					return "UInt64";

				case "Guid":
					return "TGuid";

				case "object":
					return "TObject";

				case "decimal":
				case "Decimal":
					return "Decimal";

				case "Double":
				case "double":
					return "Double";

				case "float":
				case "Float":
					return "Single";

				case "byte":
				case "Byte":
					return "Byte";

				case "byte*":
					return "PByte";

				case "sbyte":
				case "SByte":
					return "Int8";

				case "string":
				case "String":
					return "string";

				case "short":
				case "Short":
					return "Int16";

				case "Type":
				case "TType":
					return "TRttiType";

					
				case "TIntPtr":
				case "IntPtr":
					return "Pointer";

				//case "Rectangle":
				//case "TRectangle":
				//	return "TRect";

				case "ushort":
					return "UInt16";

				case "char":
					return "Char";

				case "List":
					return "TList";

				case "ReadOnlyCollection":
					return "TReadOnlyCollection";
                case "Pointer":
                    return "Pointer";


                default:
					if (pTypeName.Length < 2)
						return pTypeName;
					else if (pTypeName.StartsWith("I") && Char.IsUpper(pTypeName[1]))
						return pTypeName;
					else if (pTypeName.StartsWith("T") && Char.IsUpper(pTypeName[1]))
						return pTypeName;
					else if (pTypeName.EndsWith("Exception"))
						return "E" + pTypeName;
					else
					{
                        return "T" + pTypeName.Replace(".", ".T");
                    }
						
			}
		}

	}

